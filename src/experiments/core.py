class MCTSInfer(BaseMCTS):
    """
    UCT inference with pluggable scoring:
      - "prm": V(s) from PRM on state text (step prefixes)
      - "logprob": value from policy logprob (per-step; path-level at leaf)
      - "orm": value only at terminal from ORM (end-of-answer)
    Tracks token usage (prompt, generated, scorer).
    """

    def __init__(
        self,
        mas: MAS,
        question: str,
        score_fn: Optional[Callable[[str], float]] = None,  # PRM or ORM
        *,
        score_type: str = "prm",  # "prm" | "logprob" | "orm"
        prm_tokenizer: Optional[Any] = None,
        orm_tokenizer: Optional[Any] = None,
        max_children: int = 5,
        c_uct: float = 2.0,
        gen_kwargs: Optional[Dict[str, Any]] = None,
        step_separator: str = "</step>",
        uct_type: str = "uct",  #  "uct" | "puct"
        leaf_score_type: Optional[str] = None,  # override leaf eval, e.g., "orm"
        logprob_agg: str = "sum",  # "sum" | "avg_token" | "avg_step"
        leaf_score_fn: Optional[Callable[[str], float]] = None,  # leaf PRM/ORM
        view_mode: str = "full",  # "full" | "local" — scope of state text for PRM/ORM
    ):
        super().__init__(
            mas,
            question,
            gen_kwargs=gen_kwargs,
            root_init_value=0.0,
            root_init_visits=1,
        )
        self.q = question
        self.score_fn = score_fn
        self.score_type = score_type
        self.prm_tokenizer = prm_tokenizer
        self.orm_tokenizer = orm_tokenizer
        self.max_children = int(max(1, max_children))
        self.c = float(c_uct)
        self.sep = step_separator
        self.leaf_score_fn = leaf_score_fn or score_fn

        self.uct_type = (uct_type or "uct").lower()
        self.leaf = (leaf_score_type or "").lower() or None
        self.logprob_agg = (logprob_agg or "sum").lower()
        self.view_mode = (view_mode or "full").lower()
        self.usage = TokenStats()

    def _uct(self, parent: Node, child: Node) -> float:
        Np = max(parent.visits + 1, 1)
        Nc = child.visits + 1
        return child.q_mean + self.c * math.sqrt(math.log(Np) / Nc)

    def _puct(self, parent: Node, child: Node) -> float:
        Np = max(parent.visits, 1)
        Nc = child.visits
        return child.q_mean + self.c * child.prior * math.sqrt(Np) / (1.0 + Nc)

    def _select_score(self, parent: Node, child: Node) -> float:
        if self.uct_type == "puct":
            return self._puct(parent, child)
        return self._uct(parent, child)

    def _expand(self, node: Node) -> None:
        # With conditional edges, terminality is determined by `next_runnable`
        # rather than depth (different branches may have different lengths).
        if self._is_terminal(node):
            return
        agent_idx, inbox = self._expansion_context(node)
        msgs = self.mas.agent_messages(agent_idx, inbox)
        enc = _encode_prompt_ids(self.mas.agents[agent_idx].tok, msgs)
        p_len = len(enc)

        candidates = self.mas.sample_candidates(
            agent_idx,
            inbox,
            n_candidates=self.max_children,
            **self.gen_kwargs,
        )
        # Deduplicate identical candidate actions (same text => same action).
        dedup = []
        seen = set()
        for outs in candidates:
            t = outs[0] if outs else ""
            if t in seen:
                continue
            seen.add(t)
            dedup.append(outs)
        candidates = dedup

        self.usage.agent_runs += self.max_children

        self.usage.prompt += p_len
        for outs in candidates:
            if outs:
                self.usage.generated += _text_token_len_tok(
                    self.mas.agents[agent_idx].tok, outs[0]
                )

        vals: List[float] = []

        # Compute values for each candidate
        for outs in candidates:
            traj2 = {**node.trajectory, agent_idx: outs}

            if self.score_type == "prm":
                s_text = render_state_text(
                    self.mas,
                    self.q,
                    traj2,
                    self.sep,
                    view=self.view_mode,
                    agent_idx=agent_idx,
                )
                v = 0.0 if self.score_fn is None else float(self.score_fn(s_text))

                if self.score_fn is not None:
                    self.usage.prm_calls += 1
                if self.prm_tokenizer is not None:
                    self.usage.scorer += _text_token_len_tok(self.prm_tokenizer, s_text)

            elif self.score_type == "logprob":
                v_lp, _, g_len = compute_logprob_and_token_counts(
                    self.mas.agents[agent_idx], None, outs[0], prompt_ids=enc
                )
                self.usage.scorer += p_len + g_len
                if self.logprob_agg in ("avg_token", "avg", "mean_token"):
                    v = v_lp / max(1, g_len)
                else:  # "sum" or "avg_step" (per-step = sum here)
                    v = v_lp

            else:  # "orm" does not score partials
                v = 0.0

            vals.append(v)

        # 2) compute priors for PUCT (softmax over v_init); safe even if UCT
        priors = [1.0 / len(candidates)] * len(candidates) if candidates else []
        if self.uct_type == "puct" and candidates:
            can_logprob = getattr(self.mas.agents[agent_idx], "model", None) is not None
            if can_logprob:
                try:
                    logpriors = []
                    for outs in candidates:
                        text = outs[0] if outs else ""
                        lp, sp, sg = compute_logprob_and_token_counts(
                            self.mas.agents[agent_idx], None, text, prompt_ids=enc
                        )
                        self.usage.scorer += sp + sg
                        logpriors.append(lp / max(1, sg))

                    m = max(logpriors)
                    exps = [math.exp(lp - m) for lp in logpriors]
                    Z = sum(exps) if exps else 1.0
                    priors = [e / Z for e in exps]
                except Exception:
                    pass  # keep uniform

        # 3) create children with v_init and prior
        for v, outs, p0 in zip(vals, candidates, priors):
            node.children.append(
                self._make_child(
                    node,
                    agent_idx,
                    outs,
                    init_value=v,
                    init_visits=1,
                    prior=p0,
                )
            )

    def rollout(self) -> None:
        node = self.root
        path = [node]

        while not self._is_terminal(node):
            if not node.children:
                self._expand(node)
                if not node.children:
                    break
                node = max(
                    node.children, key=lambda ch: self._select_score(path[-1], ch)
                )
                path.append(node)
            else:
                node = max(
                    node.children, key=lambda ch: self._select_score(path[-1], ch)
                )
                path.append(node)

        leaf_traj = path[-1].trajectory
        leaf_type = self.leaf or self.score_type

        if leaf_type == "prm":
            v_leaf = path[-1].init_value  # cached score for this node

        elif leaf_type == "logprob":
            steps = [
                leaf_traj[agent_idx][0]
                for agent_idx in self.order
                if leaf_traj.get(agent_idx)
            ]
            v_leaf, u = score_path_logprob(
                self.mas, self.q, steps, agg=self.logprob_agg
            )
            self.usage.add(u)

        else:  # "orm"
            # Pick the last decided agent in topological order (the sink of
            # the realized path), not just the highest index — different in
            # dynamic graphs where conditional edges can skip agents.
            decided = [
                agent_idx for agent_idx in self.order if leaf_traj.get(agent_idx)
            ]
            leaf_last = decided[-1] if decided else (self.mas.n - 1)
            state_text = render_state_text(
                self.mas,
                self.q,
                leaf_traj,
                self.sep,
                view=self.view_mode,
                agent_idx=leaf_last,
            )
            v_leaf, u = score_orm_end(
                self.leaf_score_fn, state_text, tokenizer=self.orm_tokenizer
            )  # use leaf scorer on the same format seen during training
            self.usage.add(u)
            self.usage.prm_calls += 1

        for n in path:
            n.visits += 1
            n.q_sum += v_leaf

    def decode(self, n_simulations: int = 40) -> Tuple[str, TokenStats]:
        for _ in range(n_simulations):
            self.rollout()
        traj = {}
        node = self.root
        while not self._is_terminal(node) and node.children:
            node = max(node.children, key=lambda ch: ch.q_mean)
            traj = node.trajectory

        _, primary_out, last = self.mas._replay(self.q, set(traj), traj)
        return self.mas._aggregate_final(primary_out, last), self.usage

    def get_topk_answers(self, k: int = 5) -> List[str]:
        """
        Collect up to k final answers from the current MCTS tree, ranked by node q_mean.
        This only replays stored trajectories (no extra model calls).
        """
        if k <= 0:
            return []

        candidates: List[Tuple[float, str]] = []
        stack = [self.root]
        while stack:
            node = stack.pop()
            # Use nodes that can serve as final states (leaf or terminal) and that have a trajectory.
            if node.trajectory and self._is_terminal(node) and node.visits > 0:
                _, primary_out, last = self.mas._replay(
                    self.q,
                    set(node.trajectory),
                    node.trajectory,
                )
                ans = self.mas._aggregate_final(primary_out, last)
                candidates.append((node.q_mean, ans))
            stack.extend(node.children)

        if not candidates:
            return []

        candidates.sort(key=lambda x: x[0], reverse=True)
        return [ans for _, ans in candidates[:k]]
