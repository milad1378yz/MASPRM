@torch.inference_mode()
def compute_logprob_and_token_counts(
    agent: Agent,
    msgs=None,
    completion: str = "",
    *,
    prompt_ids=None,  # pass pre-encoded prompt to avoid re-tokenization
) -> tuple[float, int, int]:
    """
    Returns (sum_logprob, prompt_len, gen_len).
    Vectorized; avoids Python loops. If `prompt_ids` is provided (list[int] or Tensor),
    `msgs` is ignored.
    """
    tok = agent.tok
    model = agent.model
    device = next(model.parameters()).device

    # Resolve prompt_ids (B=1)
    if prompt_ids is None:
        prompt_ids = _encode_prompt_ids(tok, msgs)
    if isinstance(prompt_ids, torch.Tensor):
        prompt_ids = (
            prompt_ids.unsqueeze(0) if prompt_ids.dim() == 1 else prompt_ids
        ).to(device)
    elif isinstance(prompt_ids, list):
        prompt_ids = torch.tensor(prompt_ids, dtype=torch.long).unsqueeze(0).to(device)
    else:
        raise TypeError("prompt_ids must be list[int] or torch.Tensor")

    comp_ids = tok(completion, return_tensors="pt", add_special_tokens=False)[
        "input_ids"
    ].to(device)
    input_ids = torch.cat([prompt_ids, comp_ids], dim=1)  # [1, L]
    attn = torch.ones_like(input_ids, device=device)

    out = model(input_ids=input_ids, attention_mask=attn)
    logits = out.logits[:, :-1, :]  # next-token logits, [1, L-1, V]
    target = input_ids[:, 1:]  # tokens to predict, [1, L-1]

    prompt_len = int(prompt_ids.shape[1])
    gen_len = int(comp_ids.shape[1])
    start = max(0, prompt_len - 1)  # first position predicting gen token 0
    end = start + gen_len

    # Vectorized gather of logprobs for generated tokens
    lp_slice = F.log_softmax(logits[0, start:end, :], dim=-1)  # [g, V]
    tgt_slice = target[0, start:end].unsqueeze(-1)  # [g, 1]
    s = lp_slice.gather(-1, tgt_slice).sum().item()  # scalar

    return float(s), prompt_len, gen_len


@torch.inference_mode()
def compute_batch_logprob_and_token_counts(
    agent: Agent,
    msgs,
    completions: Sequence[str],
    *,
    prompt_ids: Optional[Sequence[int]] = None,  # reuse a pre-encoded prompt
) -> tuple[List[float], int, List[int]]:
    """Score same-prompt completions in one teacher-forced model call."""
    if not completions:
        return [], 0, []

    tok = agent.tok
    model = agent.model
    device = next(model.parameters()).device
    if prompt_ids is None:
        prompt_ids = _encode_prompt_ids(tok, msgs)
    elif isinstance(prompt_ids, torch.Tensor):
        prompt_ids = prompt_ids.flatten().tolist()
    else:
        prompt_ids = list(prompt_ids)

    completion_ids = [
        tok(str(text), add_special_tokens=False)["input_ids"] for text in completions
    ]
    prompt_len = len(prompt_ids)
    lengths = [len(ids) for ids in completion_ids]
    max_len = prompt_len + max(lengths, default=0)
    if max_len < 2:
        return [0.0] * len(completions), prompt_len, lengths

    pad_id = tok.pad_token_id
    if pad_id is None:
        pad_id = tok.eos_token_id if tok.eos_token_id is not None else 0
    input_ids = torch.full(
        (len(completions), max_len),
        int(pad_id),
        dtype=torch.long,
        device=device,
    )
    attention_mask = torch.zeros_like(input_ids)
    for row, ids in enumerate(completion_ids):
        sequence = [*prompt_ids, *ids]
        seq_len = len(sequence)
        if sequence:
            input_ids[row, :seq_len] = torch.tensor(
                sequence, dtype=torch.long, device=device
            )
            attention_mask[row, :seq_len] = 1
    max_completion_len = max(lengths, default=0)
    if max_completion_len == 0:
        return [0.0] * len(completions), prompt_len, lengths
    logit_positions = torch.arange(
        prompt_len - 1,
        prompt_len - 1 + max_completion_len,
        device=device,
    )
    forward_kwargs = {
        "input_ids": input_ids,
        "attention_mask": attention_mask,
        "use_cache": False,
    }
    try:
        logits = model(
            **forward_kwargs,
            logits_to_keep=logit_positions,
        ).logits
    except TypeError:
        # Compatibility fallback for non-Qwen causal LMs.
        logits = model(**forward_kwargs).logits[:, logit_positions, :]

    targets = input_ids[:, prompt_len : prompt_len + max_completion_len]
    completion_mask = (
        torch.arange(max_completion_len, device=device).unsqueeze(0)
        < torch.tensor(lengths, device=device).unsqueeze(1)
    )
    scores = torch.zeros(len(completions), dtype=torch.float32, device=device)
    # Upcast a small time slice at once; upcasting the full B x T x vocab
    # tensor can otherwise consume several extra GB for Qwen's large vocabulary.
    for start in range(0, max_completion_len, 32):
        end = min(max_completion_len, start + 32)
        logits_chunk = logits[:, start:end, :].float()
        targets_chunk = targets[:, start:end]
        target_logits = logits_chunk.gather(
            -1, targets_chunk.unsqueeze(-1)
        ).squeeze(-1)
        token_logprobs = target_logits - torch.logsumexp(logits_chunk, dim=-1)
        scores += (token_logprobs * completion_mask[:, start:end]).sum(dim=1)
    return [float(value) for value in scores.tolist()], prompt_len, lengths


def score_path_logprob(
    mas,
    question: str,
    steps: list[str],
    *,
    agg: str = "sum",
) -> tuple[float, TokenStats]:
    usage = TokenStats()
    traj_so_far: Dict[int, List[str]] = {}
    s_total = 0.0
    token_total = 0

    # Walk the dynamic graph: the k-th provided step belongs to whichever
    # agent is runnable given the trajectory so far (in a static DAG this
    # collapses to agent_order[k], matching the old behavior).
    for text in steps:
        result = mas.next_runnable(question, traj_so_far)
        if result is None:
            break
        agent_idx, inbox = result
        msgs = mas.agent_messages(agent_idx, inbox)

        lp, p_len, g_len = compute_logprob_and_token_counts(
            mas.agents[agent_idx], msgs, text
        )
        usage.scorer += p_len + g_len
        s_total += lp
        token_total += max(0, g_len)

        traj_so_far[agent_idx] = [text]

    if agg in ("avg_token", "avg", "mean_token"):
        value = s_total / max(1, token_total)
    elif agg in ("avg_step", "mean_step"):
        value = s_total / max(1, len(steps))
    else:  # "sum" (default)
        value = s_total

    return value, usage


def score_path_prm_product(
    score_fn,
    mas,
    question: str,
    steps: list[str],
    tokenizer=None,
    step_separator: str = "</step>",
    *,
    view_mode: str = "full",
) -> tuple[float, TokenStats]:
    usage = TokenStats()
    traj: Dict[int, List[str]] = {}
    product = 1.0
    for text in steps:
        result = mas.next_runnable(question, traj)
        if result is None:
            break
        agent_idx, _inbox = result
        traj[agent_idx] = [text]
        s_text = render_state_text(
            mas, question, traj, step_separator, view=view_mode, agent_idx=agent_idx
        )
        v = float(score_fn(s_text))  # [-1, 1]
        usage.prm_calls += 1
        w = (v + 1.0) / 2.0
        product *= max(1e-6, w)
        if tokenizer is not None:
            usage.scorer += _text_token_len_tok(tokenizer, s_text)
    return product, usage


def score_orm_end(orm_fn, text: str, tokenizer=None) -> tuple[float, TokenStats]:
    usage = TokenStats()
    v = float(orm_fn(text))  # expect ~[-1, 1]
    if tokenizer is not None:
        usage.scorer += _text_token_len_tok(tokenizer, text)
    return v, usage


