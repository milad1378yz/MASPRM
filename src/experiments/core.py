@dataclass
class Runtime:
    model: Any
    tokenizer: AutoTokenizer


def _ensure_pad_token(tokenizer: AutoTokenizer) -> AutoTokenizer:
    if tokenizer.pad_token is None and tokenizer.eos_token is not None:
        tokenizer.pad_token = tokenizer.eos_token
    return tokenizer


def build_runtime(
    model_id: str,
    *,
    tokenizer: Optional[AutoTokenizer] = None,
    torch_dtype: torch.dtype = torch.float16,
    device_map: Optional[str] = "auto",
    load_in_4bit: bool = False,
    attn_impl: str = "sdpa",
    compile_model: bool = False,
) -> Runtime:
    model, tok = load_generation_model(
        model_id,
        tokenizer=tokenizer,
        torch_dtype=torch_dtype,
        device_map=device_map,
        load_in_4bit=load_in_4bit,
        attn_impl=attn_impl,
        compile_model=compile_model,
    )
    return Runtime(model=model, tokenizer=tok)


def load_prm_scorer(
    prm_dir: str,
    base_model_id: str = None,
    max_length: int = 2048,
    torch_dtype: torch.dtype = torch.float16,
    step_separator: str = "</step>",
):
    """
    Returns: (score: Callable[[str], float], tokenizer, model)
    Loads a token-classification base + LoRA PRM adapter.
    Score = tanh(last-token-logit) in [-1, 1].
    """
    adapter_dir = Path(prm_dir)
    if not (adapter_dir / "adapter_config.json").exists():
        ckpts = sorted(adapter_dir.glob("checkpoint-*"), key=lambda p: p.stat().st_mtime)
        if ckpts and (ckpts[-1] / "adapter_config.json").exists():
            adapter_dir = ckpts[-1]
    adapter_dir = adapter_dir.resolve()

    # Resolve base model if not supplied
    if base_model_id is None:
        cfg = PeftConfig.from_pretrained(str(adapter_dir))
        base_model_id = getattr(cfg, "base_model_name_or_path", None) or "Qwen/Qwen2.5-7B-Instruct"

    # Tokenizer: prefer saved with adapter; else base
    tok_src = str(adapter_dir) if (adapter_dir / "tokenizer.json").exists() else base_model_id
    tok = _ensure_pad_token(
        AutoTokenizer.from_pretrained(tok_src, use_fast=True, trust_remote_code=True)
    )

    # Base model with PRM head
    bnb_cfg = BitsAndBytesConfig(
        load_in_4bit=True,
        bnb_4bit_quant_type="nf4",
        bnb_4bit_use_double_quant=True,
        bnb_4bit_compute_dtype=torch.float16,
    )

    base = AutoModelForTokenClassification.from_pretrained(
        base_model_id,
        num_labels=1,
        device_map="auto",
        trust_remote_code=True,
        quantization_config=bnb_cfg,
    )

    # Align embeddings to tokenizer before loading adapter
    if len(tok) != base.get_input_embeddings().weight.size(0):
        base.resize_token_embeddings(len(tok))

    # Attach LoRA adapter
    model = PeftModel.from_pretrained(base, str(adapter_dir))
    model.eval()
    device = next(model.parameters()).device

    @torch.inference_mode()
    def score(text: str) -> float:
        inputs = tok(text, return_tensors="pt", truncation=True, max_length=max_length)
        inputs = {k: v.to(device) for k, v in inputs.items()}
        out = model(**inputs)  # logits: (1, T, 1)
        input_ids = inputs["input_ids"][0]
        sep_ids = tok.encode(step_separator, add_special_tokens=False)
        if len(sep_ids) == 1:
            sep_pos = (input_ids == sep_ids[0]).nonzero(as_tuple=True)[0]
            idx = int(sep_pos[-1].item()) if sep_pos.numel() > 0 else int(input_ids.shape[0] - 1)
        else:
            idx = int(input_ids.shape[0] - 1)
        z = out.logits[0, idx, 0].float()
        return float(torch.tanh(z).item())

    return score, tok, model


def load_generation_model(
    base_model_id: str,
    tokenizer: Optional[AutoTokenizer] = None,
    torch_dtype: torch.dtype = torch.float16,
    *,
    device_map: Optional[str] = "auto",
    load_in_4bit: bool = False,
    attn_impl: str = "sdpa",
    compile_model: bool = False,
):
    """
    Loads a Causal LM for generation. If `tokenizer` provided, embeddings are resized accordingly.
    """
    if tokenizer is None:
        tokenizer = AutoTokenizer.from_pretrained(base_model_id, use_fast=True, trust_remote_code=True)
    tokenizer = _ensure_pad_token(tokenizer)

    kwargs: Dict[str, Any] = {
        "torch_dtype": torch_dtype,
        "trust_remote_code": True,
    }
    if device_map is not None:
        kwargs["device_map"] = device_map
    if attn_impl:
        kwargs["attn_implementation"] = attn_impl
    if load_in_4bit:
        compute_dtype = (
            torch_dtype if torch_dtype in (torch.float16, torch.bfloat16) else torch.float16
        )
        kwargs["quantization_config"] = BitsAndBytesConfig(
            load_in_4bit=True,
            bnb_4bit_compute_dtype=compute_dtype,
            bnb_4bit_use_double_quant=True,
        )
        kwargs["torch_dtype"] = compute_dtype

    model = AutoModelForCausalLM.from_pretrained(
        base_model_id,
        **kwargs,
    )
    if len(tokenizer) != model.get_input_embeddings().weight.size(0):
        model.resize_token_embeddings(len(tokenizer))
    model.eval()

    if compile_model:
        try:
            model = torch.compile(model, mode="reduce-overhead", fullgraph=False)
        except Exception as exc:
            print(f"[warn] torch.compile failed: {exc}. Continuing without compile().")

    return model, tokenizer


def ensure_separator_token(tokenizer: AutoTokenizer, sep: str) -> int:
    """
    Adds `sep` to vocab as additional special token if missing. Returns 0/1 tokens added.
    """
    added = 0
    added_vocab = tokenizer.get_added_vocab()
    if sep not in added_vocab:
        added += tokenizer.add_special_tokens({"additional_special_tokens": [sep]})
        _ensure_pad_token(tokenizer)
    return added


def align_model_to_tokenizer(model, tokenizer) -> None:
    """Resize model embeddings to match tokenizer size if needed."""
    if len(tokenizer) != model.get_input_embeddings().weight.size(0):
        model.resize_token_embeddings(len(tokenizer))


def render_state_text(
    mas: Any,
    question: str,
    trajectory: Dict[int, List[str]],
    step_separator: str = "</step>",
    *,
    view: str = "full",
    agent_idx: Optional[int] = None,
) -> str:
    """Build the text the scorer sees.

    view="full" (default, routed transcript — matches PRM training format):
        question + step_1 + sep + ... + step_n + sep

    view="local" (agent-local scope):
        Only the inputs routed to `agent_idx` (its inbox from parents, which
        includes the question iff it is an entry agent) followed by that
        agent's own output. Nothing from siblings or other branches of the
        DAG leaks in. If `agent_idx` is None, the deepest decided agent is
        used.
    """
    if view == "full":
        # Include every decided agent in topological order. In a dynamic
        # graph some agents may be skipped by conditional edges and still
        # have downstream decisions; don't stop at the first gap.
        parts = [question]
        for agent_idx in _agent_order(mas):
            if agent_idx in trajectory and trajectory[agent_idx]:
                parts.append(trajectory[agent_idx][0] + step_separator)
        return "".join(parts)

    if view == "local":
        # Local view = only this agent's routed inputs + its own output,
        # terminated by ONE separator so the scorer's last-sep logit aligns
        # with the single labeled separator in training records of the form
        # {prompt=<agent_inputs>, completions=[<agent_output>], labels=[q]}.
        order = _agent_order(mas)
        position = _agent_position(mas)
        if agent_idx is None:
            decided = [idx for idx in order if idx in trajectory and trajectory[idx]]
            if not decided:
                return question + step_separator
            agent_idx = decided[-1]

        cutoff = position[agent_idx]
        prior = {
            idx: trajectory[idx]
            for idx in order[:cutoff]
            if idx in trajectory and trajectory[idx]
        }
        inbox, _, _ = mas._replay(question, set(prior), prior)

        user_content = mas.agent_user_content(agent_idx, inbox)
        output = (
            trajectory[agent_idx][0]
            if agent_idx in trajectory and trajectory[agent_idx]
            else ""
        )
        # Training records are {prompt=user_content, completions=[output], labels=[q]},
        # which TRL tokenizes as tok(user_content)+tok(output)+[sep] with no delimiter
        # in between. Match that here — do not inject "\n\n".
        if user_content and output:
            return user_content + output + step_separator
        if output:
            return output + step_separator
        if user_content:
            return user_content + step_separator
        return question + step_separator

    raise ValueError(f"Unknown view mode: {view!r}")


# =========================================================
#                       MCTS inference
# =========================================================


class MCTSInfer(BaseMCTS):
    """
    UCT inference with pluggable scoring:
      - "prm": V(s) from PRM on state text (step prefixes)
      - "logprob": value from policy logprob (per-step; path-level at leaf)
      - "orm": value only at terminal from ORM (end-of-answer)
    Tracks token usage (prompt, generated, scorer).
    """

    def __init__(
        self,
        mas: MAS,
        question: str,
        score_fn: Optional[Callable[[str], float]] = None,  # PRM or ORM
        *,
        score_type: str = "prm",  # "prm" | "logprob" | "orm"
        prm_tokenizer: Optional[Any] = None,
        orm_tokenizer: Optional[Any] = None,
        max_children: int = 5,
        c_uct: float = 2.0,
        gen_kwargs: Optional[Dict[str, Any]] = None,
        step_separator: str = "</step>",
        uct_type: str = "uct",  #  "uct" | "puct"
        leaf_score_type: Optional[str] = None,  # override leaf eval, e.g., "orm"
        logprob_agg: str = "sum",  # "sum" | "avg_token" | "avg_step"
        leaf_score_fn: Optional[Callable[[str], float]] = None,  # leaf PRM/ORM
        view_mode: str = "full",  # "full" | "local" — scope of state text for PRM/ORM
    ):
        super().__init__(
            mas,
            question,
            gen_kwargs=gen_kwargs,
            root_init_value=0.0,
            root_init_visits=1,
        )
        self.q = question
        self.score_fn = score_fn
        self.score_type = score_type
        self.prm_tokenizer = prm_tokenizer
        self.orm_tokenizer = orm_tokenizer
        self.max_children = int(max(1, max_children))
        self.c = float(c_uct)
        self.sep = step_separator
        self.leaf_score_fn = leaf_score_fn or score_fn

        self.uct_type = (uct_type or "uct").lower()
        self.leaf = (leaf_score_type or "").lower() or None
        self.logprob_agg = (logprob_agg or "sum").lower()
        self.view_mode = (view_mode or "full").lower()
        self.usage = TokenStats()

    def _uct(self, parent: Node, child: Node) -> float:
        Np = max(parent.visits + 1, 1)
        Nc = child.visits + 1
        return child.q_mean + self.c * math.sqrt(math.log(Np) / Nc)

    def _puct(self, parent: Node, child: Node) -> float:
        Np = max(parent.visits, 1)
        Nc = child.visits
        return child.q_mean + self.c * child.prior * math.sqrt(Np) / (1.0 + Nc)

    def _select_score(self, parent: Node, child: Node) -> float:
        if self.uct_type == "puct":
            return self._puct(parent, child)
        return self._uct(parent, child)

    def _expand(self, node: Node) -> None:
        d = self._depth_of(node)
        if d >= len(self.order):
            return

        agent_idx, inbox = self._expansion_context(node)
        msgs = self.mas.agent_messages(agent_idx, inbox)
        enc = self.mas.agents[agent_idx].tok.apply_chat_template(
            msgs, tokenize=True, add_generation_prompt=True
        )
        p_len = (
            len(enc)
            if isinstance(enc, list)
            else (
                int(
                    enc.numel()
                    if isinstance(enc, torch.Tensor) and enc.dim() == 1
                    else enc.shape[1]
                )
            )
        )

        candidates = self.mas.sample_candidates(
            agent_idx,
            inbox,
            n_candidates=self.max_children,
            **self.gen_kwargs,
        )
        # Deduplicate identical candidate actions (same text => same action).
        dedup = []
        seen = set()
        for outs in candidates:
            t = outs[0] if outs else ""
            if t in seen:
                continue
            seen.add(t)
            dedup.append(outs)
        candidates = dedup

        self.usage.agent_runs += self.max_children

        self.usage.prompt += p_len
        for outs in candidates:
            if outs:
                self.usage.generated += _text_token_len_tok(self.mas.agents[agent_idx].tok, outs[0])

        vals: List[float] = []

        # Compute values for each candidate
        for outs in candidates:
            traj2 = {**node.trajectory, agent_idx: outs}

            if self.score_type == "prm":
                s_text = render_state_text(
                    self.mas,
                    self.q,
                    traj2,
                    self.sep,
                    view=self.view_mode,
                    agent_idx=agent_idx,
                )
                v = 0.0 if self.score_fn is None else float(self.score_fn(s_text))

                if self.score_fn is not None:
                    self.usage.prm_calls += 1
                if self.prm_tokenizer is not None:
                    self.usage.scorer += _text_token_len_tok(self.prm_tokenizer, s_text)

            elif self.score_type == "logprob":
                v_lp, _, g_len = compute_logprob_and_token_counts(
                    self.mas.agents[agent_idx], None, outs[0], prompt_ids=enc
                )
                self.usage.scorer += p_len + g_len
                if self.logprob_agg in ("avg_token", "avg", "mean_token"):
                    v = v_lp / max(1, g_len)
                else:  # "sum" or "avg_step" (per-step = sum here)
                    v = v_lp

            else:  # "orm" does not score partials
                v = 0.0

            vals.append(v)

        # 2) compute priors for PUCT (softmax over v_init); safe even if UCT
        priors = [1.0 / len(candidates)] * len(candidates) if candidates else []
        if self.uct_type == "puct" and candidates:
            can_logprob = getattr(self.mas.agents[agent_idx], "model", None) is not None
            if can_logprob:
                try:
                    logpriors = []
                    for outs in candidates:
                        text = outs[0] if outs else ""
                        lp, sp, sg = compute_logprob_and_token_counts(
                            self.mas.agents[agent_idx], None, text, prompt_ids=enc
                        )
                        self.usage.scorer += sp + sg
                        logpriors.append(lp / max(1, sg))

                    m = max(logpriors)
                    exps = [math.exp(lp - m) for lp in logpriors]
                    Z = sum(exps) if exps else 1.0
                    priors = [e / Z for e in exps]
                except Exception:
                    pass  # keep uniform

        # 3) create children with v_init and prior
        for v, outs, p0 in zip(vals, candidates, priors):
            node.children.append(
                self._make_child(
                    node,
                    agent_idx,
                    outs,
                    init_value=v,
                    init_visits=1,
                    prior=p0,
                )
            )

    def rollout(self) -> None:
        node = self.root
        path = [node]

        while not self._is_terminal(node):
            if not node.children:
                self._expand(node)
                if not node.children:
                    break
                node = max(node.children, key=lambda ch: self._select_score(path[-1], ch))
                path.append(node)
            else:
                node = max(node.children, key=lambda ch: self._select_score(path[-1], ch))
                path.append(node)

        leaf_traj = path[-1].trajectory
        leaf_type = self.leaf or self.score_type

        if leaf_type == "prm":
            v_leaf = path[-1].init_value  # cached score for this node

        elif leaf_type == "logprob":
            steps = [leaf_traj[agent_idx][0] for agent_idx in self.order if leaf_traj.get(agent_idx)]
            v_leaf, u = score_path_logprob(self.mas, self.q, steps, agg=self.logprob_agg)
            self.usage.add(u)

        else:  # "orm"
            # Pick the last decided agent in topological order (the sink of
            # the realized path), not just the highest index — different in
            # dynamic graphs where conditional edges can skip agents.
            decided = [agent_idx for agent_idx in self.order if leaf_traj.get(agent_idx)]
            leaf_last = decided[-1] if decided else (self.mas.n - 1)
            state_text = render_state_text(
                self.mas,
                self.q,
                leaf_traj,
                self.sep,
                view=self.view_mode,
                agent_idx=leaf_last,
            )
            v_leaf, u = score_orm_end(
                self.leaf_score_fn, state_text, tokenizer=self.orm_tokenizer
            )  # use leaf scorer on the same format seen during training
            self.usage.add(u)
            self.usage.prm_calls += 1

        for n in path:
            n.visits += 1
            n.q_sum += v_leaf

    def decode(self, n_simulations: int = 40) -> Tuple[str, TokenStats]:
        for _ in range(n_simulations):
            self.rollout()
        traj = {}
        node = self.root
        while not self._is_terminal(node) and node.children:
            node = max(node.children, key=lambda ch: ch.q_mean)
            traj = node.trajectory

        _, primary_out, last = self.mas._replay(self.q, set(traj), traj)
        return self.mas._aggregate_final(primary_out, last), self.usage

    def get_topk_answers(self, k: int = 5) -> List[str]:
        """
        Collect up to k final answers from the current MCTS tree, ranked by node q_mean.
        This only replays stored trajectories (no extra model calls).
        """
        if k <= 0:
            return []

        candidates: List[Tuple[float, str]] = []
        stack = [self.root]
        while stack:
            node = stack.pop()
            # Use nodes that can serve as final states (leaf or terminal) and that have a trajectory.
            if node.trajectory and self._is_terminal(node) and node.visits > 0:
                _, primary_out, last = self.mas._replay(
                    self.q,
                    set(node.trajectory),
                    node.trajectory,
                )
                ans = self.mas._aggregate_final(primary_out, last)
                candidates.append((node.q_mean, ans))
            stack.extend(node.children)

        if not candidates:
            return []

        candidates.sort(key=lambda x: x[0], reverse=True)
        return [ans for _, ans in candidates[:k]]
