def sbs_decode2(
    mas: Any,
    question: str,
    score_type: str = "none",  # "none" | "prm" | "logprob"
    score_fn: Optional[Callable[[str], float]] = None,  # PRM for "prm"
    prm_tokenizer: Optional[Any] = None,  # tokenizer for PRM (token counting)
    B1: int = 1,
    B2: int = 5,
    gen_kwargs: Optional[Dict[str, Any]] = None,
    step_separator: str = "</step>",
    trace: Optional[List[Dict[str, Any]]] = None,
    return_trace: bool = False,
    *,
    logprob_agg: str = "sum",  # "sum" | "avg_token" | "avg_step"
    final_answers_out: Optional[List[str]] = None,  # collect beam finals for pass@k
    view_mode: str = "full",  # "full" | "local" — PRM state scope
):

    usage = TokenStats()
    gen_kwargs = gen_kwargs or {}
    agent_order = _agent_order(mas)
    beams: List[Tuple[Dict[int, List[str]], float]] = [({}, 0.0)]
    local_trace: List[Dict[str, Any]] = (
        [] if (trace is not None or return_trace) else None
    )

    # Walk agent_order (topological). For each agent, only beams that have
    # its required parents delivered (is_runnable) actually produce
    # candidates — other beams are carried forward unchanged. This lets
    # different beams traverse different subsets of the DAG when
    # conditional edges suppress dispatch.
    for agent_idx in agent_order:
        new_beams: List[Tuple[Dict[int, List[str]], float]] = []
        cand_infos_all: List[Tuple[List[str], float]] = []
        any_beam_ran = False

        for traj, prev_score in beams:
            inbox, _, _ = mas._replay(question, set(traj), traj)

            if not mas.is_runnable(agent_idx, inbox) or agent_idx in traj:
                # Beam skips this agent (conditional edge suppressed, or
                # somehow already decided). Carry it forward unchanged.
                new_beams.append((traj, prev_score))
                continue

            any_beam_ran = True
            msgs = mas.agent_messages(agent_idx, inbox)

            # PRE-ENCODE prompt once; reuse for accounting and logprob scoring
            enc = _encode_prompt_ids(mas.agents[agent_idx].tok, msgs)
            p_len = len(enc)

            # Generate candidates
            cands = mas.sample_candidates(
                agent_idx, inbox, n_candidates=B2, **gen_kwargs
            )
            usage.agent_runs += B2

            # Account policy tokens (prompt + generated)
            usage.prompt += p_len
            for outs in cands:
                if outs:
                    usage.generated += _text_token_len_tok(
                        mas.agents[agent_idx].tok, outs[0]
                    )

            scored_cands = cands[:1] if score_type == "none" else cands
            cand_infos: List[Tuple[List[str], float]] = []

            for outs in scored_cands:
                if score_type == "prm":
                    traj2 = {**traj, agent_idx: outs}
                    s_text = render_state_text(
                        mas,
                        question,
                        traj2,
                        step_separator,
                        view=view_mode,
                        agent_idx=agent_idx,
                    )
                    v = 0.0 if score_fn is None else float(score_fn(s_text))
                    if score_fn is not None:
                        usage.prm_calls += 1
                    if prm_tokenizer is not None:
                        usage.scorer += _text_token_len_tok(prm_tokenizer, s_text)
                elif score_type == "logprob":
                    s, sp, sg = compute_logprob_and_token_counts(
                        mas.agents[agent_idx], None, outs[0], prompt_ids=enc
                    )
                    usage.scorer += sp + sg
                    if logprob_agg in ("avg_token", "avg", "mean_token"):
                        v = s / max(1, sg)
                    elif logprob_agg in ("avg_step", "mean_step"):
                        # one step => same as "sum"
                        v = s
                    else:
                        v = s
                else:
                    v = 0.0
                cand_infos.append((outs, v))
                cand_infos_all.append((outs, v))

            for outs, v in cand_infos:
                new_beams.append(({**traj, agent_idx: outs}, v))

        if local_trace is not None and any_beam_ran:
            local_trace.append(
                {
                    "agent_idx": agent_idx,
                    "candidates": [
                        {
                            "text": (outs[0] if outs else ""),
                            "score": (float(v) if score_type != "none" else None),
                        }
                        for outs, v in cand_infos_all
                    ],
                    "chosen_text": None,
                    "chosen_score": None if score_type == "none" else float("-inf"),
                }
            )

        if score_type != "none":
            new_beams.sort(key=lambda x: x[1], reverse=True)
        beams = new_beams[: max(1, B1)]

        if local_trace is not None and any_beam_ran:
            chosen_traj, chosen_val = beams[0]
            chosen_outs = chosen_traj.get(agent_idx)
            # If the top beam was a carry-forward (didn't run this agent),
            # leave chosen_text as None so downstream consumers skip it.
            if chosen_outs:
                local_trace[-1]["chosen_text"] = chosen_outs[0]
                local_trace[-1]["chosen_score"] = (
                    None if score_type == "none" else float(chosen_val)
                )

    # collect final answers for all surviving beams (for pass@k)
    if final_answers_out is not None:
        final_answers_out.clear()
        for traj, _ in beams:
            _, primary_out_i, last_i = mas._replay(question, set(traj), traj)
            final_answers_out.append(mas._aggregate_final(primary_out_i, last_i))

    best_traj, _ = beams[0]
    inbox, primary_out, last = mas._replay(question, set(best_traj), best_traj)
    final_answer = mas._aggregate_final(primary_out, last)

    if trace is not None:
        trace.extend(local_trace or [])
    if return_trace:
        return final_answer, usage, (local_trace or [])
    return final_answer, usage


